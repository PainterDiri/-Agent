function hashText(text) {
  let hash = 17;
  for (const character of text) hash = (hash * 31 + character.codePointAt(0)) >>> 0;
  return hash;
}

export function selectFortuneTechnique(messages = []) {
  const firstUserText = messages.find((message) => message.role === "user")?.content || "梁山福铺";
  return TECHNIQUES[hashText(firstUserText) % TECHNIQUES.length];
}

